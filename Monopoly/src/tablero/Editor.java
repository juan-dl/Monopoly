    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String cadena = null;
        
        if((cadena = reglasTablero()) == null)
        {
            ArrayList<Grupo> grupos = new ArrayList<>();
            Tablero nuevo = new Tablero();
            Casilla aux = null;

            nuevo.setFiguras(partida.getTablero().getFiguras());
            nuevo.setCasillas(casillas);

            for(ArrayList<Casilla> lado : casillas)
                for(Casilla casilla : lado)
                {
                    if(casilla instanceof Propiedad)
                        ((Propiedad)casilla).setPropietario(nuevo.getBanca());
                    
                    if(casilla instanceof Solar && !grupos.contains(((Solar)casilla).getGrupo()))
                        grupos.add(((Solar)casilla).getGrupo());

                    if(casilla instanceof Especial && ((Especial)casilla).getTipo().equals("Salida"))
                        aux = casilla;
                }

            nuevo.setGrupos(grupos);

            for(Avatar a : partida.getTablero().getFiguras().values())
                a.setTablero(nuevo);

            setVisible(false);
            partida.getPartida().setVisible(false);
            
            Juego juego = new Juego(nuevo, partida.getFiguras(), partida.getOrdenTurnos());
            juego.getPartida().reiniciar();
        }
        else
            consola.imprimir(cadena);
    }//GEN-LAST:event_jButton1ActionPerformed

