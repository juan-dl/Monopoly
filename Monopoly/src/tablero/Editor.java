    private String reglasTablero()
    {
       
        boolean aux = true;
        int contadorSalida = 0, contadorCarcel = 0, contadorIrCarcel = 0, contadorParking = 0, contadorImpuesto = 0;
        int contadorSolar = 0, contadorSuerte = 0, contadorComunidad = 0, contadorTransporte = 0, contadorServicio = 0;
        
        for(ArrayList<Casilla> lado : casillas)
            for(Casilla cas : lado)
            {   
                for(ArrayList<Casilla> l : casillas)
                    for(Casilla c : l)
                        if(c != cas)
                            if(!(c instanceof Suerte && cas instanceof Suerte) && !(c instanceof CajaComunidad && cas instanceof CajaComunidad) && c.getNombre().equals(cas.getNombre()))
                                return "El nombre \"" + cas.getNombre() + "\" esta repetido";
                
                if(cas instanceof Solar)
                {
                    contadorSolar++;
                    
                    switch(((Solar) cas).getGrupo().getNombre())
                    {
                        case Constantes.NEGRO:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 2)
                                return "El grupo negro debe tener 2 casillas";
                            break;
                            
                        case Constantes.CYAN:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo cyan debe tener 3 casillas";
                            break;
                         
                        case Constantes.ROSA:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo rosa debe tener 3 casillas";
                            break;
                         
                        case Constantes.AMARILLO:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo amarillo debe tener 3 casillas";
                            break;
                          
                        case Constantes.ROJO:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo rojo debe tener 3 casillas";
                            break;
                            
                        case Constantes.MARRON:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo marron debe tener 3 casillas";
                            break;
                            
                        case Constantes.VERDE:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 3)
                                return "El grupo verde debe tener 3 casillas";
                            break;
                            
                        case Constantes.AZUL:
                            if(((Solar) cas).getGrupo().getCasillas().size() != 2)
                                return "El grupo azul debe tener 2 casillas";
                            break;
                    }
                    
                }
                else if(cas instanceof Impuesto)
                    contadorImpuesto++;
                else if(cas instanceof Servicio)
                    contadorServicio++;
                else if(cas instanceof Transporte)
                    contadorTransporte++;
                else if(cas instanceof CajaComunidad)
                    contadorComunidad++;
                else if(cas instanceof Suerte)
                    contadorSuerte++;
                else
                {
                    switch(((Especial)cas).getTipo())
                    {
                        case "Salida":
                            contadorSalida++;
                            break;
                            
                        case "Carcel":
                            contadorCarcel++;
                            break;
                            
                        case "IrCarcel":
                            contadorIrCarcel++;
                            break;
                            
                        case "Parking":
                            contadorParking++;
                            break;
                    }
                }
            }
        
        if(contadorSalida != 1)
            return "Tiene que haber una unica Salida";
        
        if(contadorCarcel != 1)
            return "Tiene que haber una unica Carcel";
        
        if(contadorIrCarcel != 1)
            return "Tiene que haber una unica casilla de Ir a la carcel";
        
        if(contadorParking != 1)
            return "Tiene que haber un unico Parking";
        
        if(contadorSolar != 22)
            return "Tiene que haber 22 Solares";
        
        if(contadorSuerte != 3)
            return "Tiene que haber 3 casillas de Suerte";
        
        if(contadorComunidad != 3)
            return "Tiene que haber 3 casillas de Caja de comunidad";
        
        if(contadorImpuesto != 2)
            return "Tiene que haber 2 casillas de Impuesto";
        
        if(contadorServicio != 2)
            return "Tiene que haber 2 casillas de Servicio";
        
        if(contadorTransporte != 4)
            return "Tiene que haber 4 casillas de Transporte";
        
        return null;
    }
    
