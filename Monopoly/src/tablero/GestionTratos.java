    public GestionTratos(VentanaJuego juego) 
    {
        ArrayList<Jugador> jugadores = new ArrayList<>();
        Jugador j;
        
        initComponents();
        setLocationRelativeTo(null);
        this.juego = juego;
        
        j = juego.getPartida().getFiguras().get(juego.getPartida().getOrdenTurnos().get(juego.getPartida().getTurnoActual())).getJugador();
        
        model1 = new DefaultListModel();
        
        for(Trato t : j.getTratosRecibidos())
            model1.addElement(t.getID());
        
        model2 = new DefaultListModel();
        
        for(Trato t : j.getTratosPropuestos())
            model2.addElement(t.getID());

        jList1.setModel(model1);
        jList2.setModel(model2);
        
        for(Avatar a : juego.getPartida().getFiguras().values())
            if(!a.getJugador().equals(j))
                jugadores.add(a.getJugador());
        
        creador = new ProponerTrato(this, jugadores, model1, model2);
        
        for(Propiedad p : j.getPropiedades())
            creador.getjComboBox3().addItem(p.getNombre());
        
        jTextArea1.setLineWrap(true);
        jTextArea1.setWrapStyleWord(true);
    }

