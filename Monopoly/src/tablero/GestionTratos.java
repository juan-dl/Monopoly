    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!jList1.isSelectionEmpty())
        {
            juego.getPartida().aceptarTrato(jList1.getSelectedValue());
            model1.removeElement(jList1.getSelectedValue());
        }
        else
            consola.imprimir("Seleccione un trato.");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(!jList2.isSelectionEmpty())
        {
            juego.getPartida().eliminarTrato(jList2.getSelectedValue());
            model1.removeElement(jList2.getSelectedValue());
        }
        else
            consola.imprimir("Seleccione un trato.");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList2MouseClicked
        if(!jList2.isSelectionEmpty())
            for(Trato trato : juego.getPartida().getFiguras().get(juego.getPartida().getOrdenTurnos().get(juego.getPartida().getTurnoActual())).getJugador().getTratosPropuestos())
                if(trato.getID().equals(jList2.getSelectedValue()))
                    jTextArea1.setText(trato + "");
    }//GEN-LAST:event_jList2MouseClicked

